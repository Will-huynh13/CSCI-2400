/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n)
{
   
    int multiplier = n << 3; // making n be multiplied by a factor of 8 which is the byte size basically 2 raised to the 3rd
    int shifting = x >> multiplier; // this line divides the by the byte size
    int masking = 0xFF; // this creates a mask that only show what is wanted 
    return (masking & shifting); // returning here will AND with the mask and will show the byte 
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */

int logicalShift(int x, int n)
{
    int bitMask = ~((1 << 31) >> n << 1); // this mask will mask out all of the leading bits
    return (x >> n) & bitMask; // AND with the mask to get the desired number also doing this if that was a negative. dont want the signed bit to change the number
}

/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */

int bitCount(int x)
{
    int maskInt = 0x11 + (0x11 << 8) + (0x11 << 16) + (0x11 << 24); // this creates a byte size mask (least sig byte for each one)
    int counter = x & maskInt; // this ANDS the mask to the number
    int maskAdd = 0x0F; // this mask gets rid of the numbers that are not important
    x = x >> 1;
    counter += x &maskInt;
    x = x >> 1;
    counter += x &maskInt;
    x = x >> 1;
    counter += x &maskInt; //counts and AND the bits
    return (counter & maskAdd) + ((counter >> 4) & maskAdd) + ((counter >> 8) & maskAdd) + ((counter >> 12) & maskAdd) 
        + ((counter >> 16) & maskAdd) + ((counter >> 20) & maskAdd) + ((counter >> 24) & maskAdd) + ((counter >> 28) & maskAdd); // adding in 4 bit sizes
}

/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */

int bang(int x)
{
    int negateX = ~x + 1; // this is using the same property as the negate function. 
    int inverse = (x | negateX) >> 31; // when the value of x is equal to 0, (x | -x) = 0. when shifting right 31 bits, it will extend the sign bit across which will leave it with a result of either a 1 or a 0 after adding the 1.
    return inverse + 1;
}

