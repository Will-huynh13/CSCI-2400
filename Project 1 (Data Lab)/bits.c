/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */

int isPositive(int x)
{
    int trackX = x >> 31; // this keeps track of the LSB of x
    trackX &= 0x1; //masking this will only keep the LSB of x
    return (!trackX) ^ (!x);
    // when the value of x is equal to 0, then it should return 0
}

/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */

int isLessOrEqual(int x, int y)
{
    int sign_of_Y;
    int sign_of_X;
    int total;
    sign_of_Y = (y >> 31) & 1; // this line copies all significant bits of y and returns a 0 if + and a 1 for -
    sign_of_X = (x >> 31 ) & 1;
    total = (!sign_of_Y ^ sign_of_X) & (((x + ~y) >> 31) & 1); // this line checks if the X and Y are the same and also checks if x is less than Y
    // if the value of X is less than Y or if they are the same and if one of the variables a negative.
    
    return total | ((!sign_of_Y) & sign_of_X);
    
}









