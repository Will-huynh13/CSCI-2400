/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */

int fitsBits(int x, int n) //basically checking if youre loosing bits
{
    int Highbits;
    int change1;
    int change2;
    Highbits = ~n + 33; // this line calculates how many high order bits are left after n low end bits
    // this is another way of saying 32 - n
    change1 =  x << Highbits; // multiply by the highbits
    change2 = change1 >> Highbits; // dividing by high bits
    return !(change2 ^ x);// checks if and only if the numbers are indentical
}

/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */

int divpwr2(int x, int n)
{
	int negInt;
    int MSB;
	MSB = (x >> 31); // this is going to get the most significant bit for the x value
	negInt = (1 << n) + (~0); // this is to access the last n digits of x
	negInt &= MSB; // this sees if it is necessary to for negative numbers
	x += negInt;
	return x>>n;
}

